import Foundation
import UIKit

extension UIColor {
    
    @nonobjc public class var borderColor: UIColor {
        return UIColor(red: 112 / 255.0, green: 112 / 255.0, blue: 112 / 255.0, alpha: 0.17)
    }
    
    @nonobjc public class var cellShadowEffect: UIColor {
        return UIColor(red: 0 / 255.0, green: 0 / 0, blue: 255 / 255.0, alpha: 0.08)
    }
    
    @nonobjc public class var appThemeColor: UIColor {
        return UIColor(red: 12 / 255.0, green: 84 / 255.0, blue: 197 / 255.0, alpha: 1)
    }
}
